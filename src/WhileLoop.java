
public class WhileLoop {

	public static void main(String[] args) {
		// initialization condition increment or decrement
		// print 1 to 10 numbers using while loop
		
		int i=1;// initialize
		while(i<=10) {// condition
		System.out.println(i);// condition is true so prints the output
		i++;// incrementing one by one
		} 
				
		

	}

}
