package StringMethods;

public class StringSubString {

	public static void main(String[] args) {
		// SubString is all about -starting index and ending index
		
		// Starting index starts from 0 and ending starts from 1

		String s="WELCOME";
		System.out.println(s.substring(0,3)); // prints WEL	
		System.out.println(s.substring(2,5));  // prints LCO
		System.out.println(s.substring(4,7)); // prints OME
	}

}
