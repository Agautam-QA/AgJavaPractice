
public class RelationalOperator {

	public static void main(String[] args) {
		// ==,<>,<=,>=,!=(result comes/returns in boolean value)
		
		int a=30;
		int b=40;
		
		System.out.println(a>b); // result comes false
		System.out.println(a<b); //true
		System.out.println(a>=b);//false
		// either greater or equal both condition false so result comes false
		System.out.println(a<=b); //true(one condition <true so result true)
		System.out.println(a==b);//false (both are not equal)
	
	// next example:(!=)
		System.out.println(a!=b);// true (a is "Not equal" b)
	
	
	}
	
	
	
	

}
