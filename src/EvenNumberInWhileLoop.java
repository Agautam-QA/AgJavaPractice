
public class EvenNumberInWhileLoop {

	public static void main(String[] args) {
		// Task: print 2 to 10 even numbers
		// to print odd number initialize with odd and increment by 2
		// to print even number initialize with even and increment by 2
		
		int i=2; //to print even , initial num even
		while(i<=10)
		{
			System.out.println(i);
			i+=2; 
			// now result comes only even numbers
		}

	}

}
